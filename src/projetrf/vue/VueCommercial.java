    private void jTextFieldCommercialCpKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCommercialCpKeyReleased
        
               try {

            List<projetrf.model.Ville> vv1 = RequetesVille.rechercheVilleByCp(jTextFieldCommercialCp.getText().trim());

            jComboBoxCommercialVille.removeAllItems();
            for (Ville ville : vv1) {
                jComboBoxCommercialVille.addItem(ville);

            }

        } catch (SQLException ex) {
            Logger.getLogger(VueVille.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTextFieldCommercialCpKeyReleased

